// Function to convert kebab-case to PascalCase for Lucide component names
function kebabToPascalCase(kebabStr: string): string {
	return kebabStr
		.split('-')
		.map((word) => word.charAt(0).toUpperCase() + word.slice(1))
		.join('')
}

