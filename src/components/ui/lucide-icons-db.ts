// Helper functions - optimized with memoization
let _allIconsCache: IconData[] | null = null
let _categoriesCache: IconCategory[] | null = null
let _lucideIconsCache: IconData[] | null = null
let _customIconsCache: IconData[] | null = null

export function getAllIcons(): IconData[] {
	if (!_allIconsCache) {
		_allIconsCache = Object.values(iconDatabase)
	}
	return _allIconsCache
}

export function getIconsByCategory(category: IconCategory): IconData[] {
	return getAllIcons().filter((icon) => icon.category === category)
}

// Optimized search with better performance for large datasets
export function searchIcons(query: string): IconData[] {
	if (!query.trim()) return []

	const searchTerm = query.toLowerCase()
	const allIcons = getAllIcons()

	// Use a more efficient search algorithm
	return allIcons.filter((icon) => {
		const name = icon.name.toLowerCase()
		const description = icon.description.toLowerCase()
		const category = icon.category.toLowerCase()

		// Quick name match first (most common)
		if (name.includes(searchTerm)) return true

		// Then description
		if (description.includes(searchTerm)) return true

		// Then category
		if (category.includes(searchTerm)) return true

		// Finally tags (most expensive)
		return icon.tags.some((tag) => tag.toLowerCase().includes(searchTerm))
	})
}

export function getIconByName(name: string): IconData | undefined {
	return iconDatabase[name]
}

export function getAllCategories(): IconCategory[] {
	if (!_categoriesCache) {
		const categories = new Set<IconCategory>()
		getAllIcons().forEach((icon) => categories.add(icon.category))
		_categoriesCache = Array.from(categories).sort()
	}
	return _categoriesCache
}

export function getCategoryDisplayName(category: IconCategory): string {
	return category
		.split('-')
		.map((word) => word.charAt(0).toUpperCase() + word.slice(1))
		.join(' ')
}

export function getLucideIconsOnly(): IconData[] {
	if (!_lucideIconsCache) {
		_lucideIconsCache = getAllIcons().filter((icon) => icon.category !== 'custom')
	}
	return _lucideIconsCache
}

export function getCustomIconsOnly(): IconData[] {
	if (!_customIconsCache) {
		_customIconsCache = getAllIcons().filter((icon) => icon.category === 'custom')
	}
	return _customIconsCache
}

